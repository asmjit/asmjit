  a.psrlw(mm0, ptr_gp0);
  a.psrlw(mm0, 0);
  a.psubb(mm0, mm7);
  a.psubb(mm0, ptr_gp0);
  a.psubw(mm0, mm7);
  a.psubw(mm0, ptr_gp0);
  a.psubd(mm0, mm7);
  a.psubd(mm0, ptr_gp0);
