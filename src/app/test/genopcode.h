  a.pf2iw(mm0, ptr_gp0);
  a.pfacc(mm0, mm7);
  a.pfacc(mm0, ptr_gp0);
  a.pfadd(mm0, mm7);
  a.pfadd(mm0, ptr_gp0);
  a.pfcmpeq(mm0, mm7);
