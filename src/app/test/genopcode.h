  a.fprem1();
  a.fptan();
  a.frndint();
  a.frstor(ptr_gp0);
  a.fsave(ptr_gp0);
  a.fscale();
  a.fsin();
  a.fsincos();
  a.fsqrt();
  a.fst(dword_ptr(gp0));
  a.fst(qword_ptr(gp0));
  a.fstp(dword_ptr(gp0));
  a.fstp(qword_ptr(gp0));
  a.fstp(tword_ptr(gp0));
  a.fstcw(ptr_gp0);
  a.fstenv(ptr_gp0);
  a.fsub(fp0, fpx);
  a.fsub(fpx, fp0);
  a.fsub(dword_ptr(gp0));
  a.fsub(qword_ptr(gp0));
  a.fsubp(fpx);
  a.fsubp();
  a.fsubr(fp0, fpx);
  a.fsubr(fpx, fp0);
  a.fsubr(dword_ptr(gp0));
  a.fsubr(qword_ptr(gp0));
  a.fsubrp(fpx);
  a.fsubrp();
  a.ftst();
